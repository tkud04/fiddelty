                        <h2>Less paper. More convenience.</h2>
<p>With Fidelity Bank’s free e-statement delivery, you can reduce the clutter in your mailbox while helping the environment. Your e-statement is an official electronic copy of your monthly account statement and can be accessed within online banking.</p>
<p>Signing up for free e-statement delivery is simple:</p>
<ol>
<li>Click on the "Statement" link under the "Accounts" tab.</li>
<li>Click on the "Statement Preferences."</li>
<li>Follow the onscreen instructions to set and confirm your delivery preferences.</li>
<li>Sit back and relax. You’ll receive an email notification when a new PDF statement is ready to view.</li>
</ol>
<p>Banking with less paper and more convenience. Just one more way we stand ready to serve.</p>
                        <div class="accordion"><button aria-controls="accordion782" aria-expanded="False" class="accordion__header collapsed" data-target="#accordion782" data-toggle="collapse" type="button">E-statements Terms and Conditions<span class="icon-plus-circle"></span><span class="icon-minus-circle"></span></button><div aria-expanded="False" class="collapse" id="accordion782"><div class="accordion__body">


<div>
<p>Your E-Statement is an official electronic copy of your monthly Checking or Savings account statement, which is accessible in a secure website via a Portable Document Format (PDF) file. A notification will be delivered to the E-mail address you specify.</p>
<p><b>System Requirements</b> – Window 7 or Higher, Mac OS 10.10.4 or higher, space to save files should you need to, Internet Service Provider (ISP) and Adobe Acrobat Reader 6.0 or higher installed on your PC in order to read the e-statements PDF (portable document Format) file. By signing/agreeing below you are stating to the Bank that your computer has met the above requirements for E-Statements.</p>
<p><b>E-Statement Service Information</b> – E-Statement delivery applies only to the monthly statement for the account(s) selected. Currently, all other notices on these accounts will be delivered on paper by mail. In the future, we may develop the ability to send additional account transaction and required reporting notices to you electronically. Your agreement to use E-Statement authorizes us to send these additional notices to you electronically in the future without further consent.</p>
<p><b>Requesting Paper Copies of Statements</b> – If you subscribe to E-Statement, you have the right to request a paper copy of your statement at any time. You may make your request by calling 1.800.658.1637 or visiting any branch location. A research fee of $2.00 will be charged to your account for each statement requested.</p>
<p><b>Termination of E-Statement Service</b> – You may withdraw your consent to receive your statement electronically at any time. You may terminate your E-Statement service by sending us a letter that lists the account numbers to be terminated and signed with your original signature to: E-Statements Fidelity Bank, P.O. Box 1007, Wichita, Kansas 67201-1007</p>
<p><b>Changing Your E-mail Address</b> – If your E-mail address changes, you must notify us immediately of your new address. You can update your email address through our secure Online Banking website under Manage Contact Information or by sending us a letter that lists you name, new email address and signed with your original signature to the address listed above. If your E-Statement notification is returned as undeliverable, we will send a paper statement to the address listed on your account. Your E-Statement service will be automatically discontinued until you notify us of your new E-mail address by resubmitting the E-statement application.</p>
<p>Terms &amp; Conditions - We have the right to update the Terms and Conditions of the E-Statement program at any time. We will notify you of any updates through Online Banking. The Terms &amp; Conditions will be located on the Statement Delivery Preference page within Online Banking for reference.</p>
<p>If you have questions about e-statements, call 1.800.658.1637 or visit any Fidelity office for assistance.</p>
